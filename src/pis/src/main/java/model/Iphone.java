public class Iphone {
    private String modelName;
    private String secondaryMemory;
    private String voltage;
    private String modelCod;
    private String iphoneLink;
    private String imageLink;
    private String displaySize;
    private String frontCam;
    private String backCam;
    private String ramMemory;
    private String title;

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public String getSecondaryMemory() {
        return secondaryMemory;
    }

    public void setSecondaryMemory(String secondaryMemory) {
        this.secondaryMemory = secondaryMemory;
    }

    public String getVoltage() {
        return voltage;
    }

    public void setVoltage(String voltage) {
        this.voltage = voltage;
    }

    public String getModelCod() {
        return modelCod;
    }

    public void setModelCod(String modelCod) {
        this.modelCod = modelCod;
    }

    public String getIphoneLink() {
        return iphoneLink;
    }

    public void setIphoneLink(String iphoneLink) {
        this.iphoneLink = iphoneLink;
    }

    public String getImageLink() {
        return imageLink;
    }

    public void setImageLink(String imageLink) {
        this.imageLink = imageLink;
    }

    public String getDisplaySize() {
        return displaySize;
    }

    public void setDisplaySize(String displaySize) {
        this.displaySize = displaySize;
    }

    public String getFrontCam() {
        return frontCam;
    }

    public void setFrontCam(String frontCam) {
        this.frontCam = frontCam;
    }

    public String getBackCam() {
        return backCam;
    }

    public void setBackCam(String backCam) {
        this.backCam = backCam;
    }

    public String getRamMemory() {
        return ramMemory;
    }

    public void setRamMemory(String ramMemory) {
        this.ramMemory = ramMemory;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}